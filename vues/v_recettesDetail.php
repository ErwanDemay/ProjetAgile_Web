                    <p>Ingrédients :

<li>1 kg de bœuf (paleron, gîte, macreuse)</li>

<li>200 g de lardons</li>

<li>3 carottes</li>

<li>2 oignons</li>

<li>2 gousses d'ail</li>

<li>1 bouquet garni (thym, laurier, persil)</li>

<li>75 cl de vin rouge (Bourgogne de préférence)</li>

<li>250 g de champignons de Paris</li>

<li>2 cuillères à soupe de farine</li>

<li>30 g de beurre</li>

<li>2 cuillères à soupe d’huile d’olive</li>

<li>Sel et poivre</li>

Étapes de préparation :

<li>Préparer les ingrédients Coupez la viande en morceaux de taille moyenne. Épluchez et coupez les carottes en rondelles, les oignons en lamelles et les champignons en quartiers. Écrasez les gousses d’ail.</li>

<li>Saisir la viande Faites chauffer l’huile dans une grande cocotte. Ajoutez les morceaux de bœuf et faites-les dorer de tous les côtés. Ajoutez ensuite les lardons et les oignons. Faites revenir quelques minutes.</li>

<li>Singer la préparation Saupoudrez la viande avec la farine et mélangez bien. Cette étape permet d’épaissir la sauce durant la cuisson.</li>

<li>Déglacer avec du vin rouge Versez le vin rouge dans la cocotte et mélangez bien pour décoller les sucs de cuisson. Ajoutez ensuite le bouquet garni et l’ail écrasé. Assaisonnez de sel et de poivre.</li>

<li>Cuire lentement Couvrez et laissez mijoter à feu doux pendant au moins 2 heures, jusqu’à ce que la viande soit tendre et fondante.</li>

<li>Ajouter les légumes et champignons Après 2 heures de cuisson, ajoutez les carottes et les champignons. Laissez mijoter encore 30 minutes.</li>

Finalisation Retirez le bouquet garni et ajustez l’assaisonnement si nécessaire. Servez bien chaud, accompagné de pommes de terre vapeur ou de tagliatelles.ss</p>

